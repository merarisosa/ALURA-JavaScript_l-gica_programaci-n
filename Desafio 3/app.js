/*
Crea un contador que comience en 1 y vaya hasta 10 usando un 
bucle 'while'. Muestra cada número.
*/
let contador = 1;
while (contador <= 10) {
  alert(`${contador}`);
  contador = contador + 1;
}

/*
Crea un contador que comience en 10 y vaya hasta 0 usando un 
bucle 'while'. Muestra cada número.
*/
let contador1 = 10;
while (contador1 >= 0) {
  alert(`${contador1}`);
  contador1 = contador1 - 1;
}

